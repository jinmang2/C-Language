#include <stdio.h>  // stdio : standard input output(표준입출력)의 줄임말
					// stdio.h의 내용을 프로그램 안에 복사

int main(void) {
	printf("%d\n", 12);     // 10진수 상수 출력
	printf("%d\n", 014);    // 8진수 상수 출력
    printf("%d\n", 0xc);    // 16진수 상수 출력
	return 0;
}
